/******************************************************
 * The use of Boolean variables and values            *
 ******************************************************/

#include <iostream>
using namespace std;

int main() {
    // Variable definitions
    bool x = 123;
    bool y = -8;
    bool z = 0;
    bool t = -0;
    bool u = true;
    bool v = false;

    // Mostrando los valores
    cout << "Value of x: " << x << endl;
    cout << "Value of y: " << y << endl;
    cout << "Value of z: " << z << endl;
    cout << "Value of t: " << t << endl;
    cout << "Value of u: " << u << endl;
    cout << "Value of v: " << v;
    
    return 0;
}